package com.mit.DB;

import java.sql.Date;

public class Review {
	private String review_id;
	private String contents_id;
	private String user_id;
	private String review_contents;
	private int review_score;
	private int review_rec; //추천수
	private Date review_date; //작성일
	
	
	public String getReview_id() {
		return review_id;
	}
	public void setReview_id(String review_id) {
		this.review_id = review_id;
	}
	public String getContents_id() {
		return contents_id;
	}
	public void setContents_id(String contents_id) {
		this.contents_id = contents_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getReview_contents() {
		return review_contents;
	}
	public void setReview_contents(String review_contents) {
		this.review_contents = review_contents;
	}
	public int getReview_score() {
		return review_score;
	}
	public void setReview_score(int review_score) {
		this.review_score = review_score;
	}
	public int getReview_rec() {
		return review_rec;
	}
	public void setReview_rec(int review_rec) {
		this.review_rec = review_rec;
	}
	public Date getReview_date() {
		return review_date;
	}
	public void setReview_date(Date review_date) {
		this.review_date = review_date;
	}

		
}
